##Introducing while Loops
#-While loops run as long as , or while, a certain condition is true

##The while Loop in Action
#-While loops can be used to count up through a series of numbers

current_number = 1
while current_number <=5:
    print(current_number)
    current_number += 1
##(The += operator is shorthand for current_number = current_number + 1.)



