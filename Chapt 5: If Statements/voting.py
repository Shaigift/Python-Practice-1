##if=else Statements
age = 17
if age >= 18:
    print("You are old enough to vote!")
    print("Have you registed to vote yet?")
else:
    print("Sorry, you are too young to vote.")
    print("Please register to vote as soon as you turn 18!")


##The if-elif-else Chain
#Python excutes only one block in an if-elif-else chain, it runs each individual test in order until one passes. when test is passed , the code following that test is executed and python skips the rest of the tests



